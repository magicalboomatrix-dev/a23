// Helper function to get bilingual text (English/Hindi together)
export function getBilingualText(obj) {
  if (!obj) return "";
  if (typeof obj === "string") return obj;
  if (obj.en && obj.hi) {
    return `${obj.en} / ${obj.hi}`;
  }
  return obj.en || obj.hi || "";
}

// Helper function to get text for specific language
export function getText(obj, lang = "bilingual") {
  if (!obj) return "";
  if (typeof obj === "string") return obj;
  
  if (lang === "bilingual") {
    return getBilingualText(obj);
  }
  
  return obj[lang] || obj.en || obj.hi || "";
}
